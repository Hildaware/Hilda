                        </li>
                        <li>
                            Export - Copies the priority in a base64 encoded string to your clipboard. This allows you to copy priorities across sets
                        </li>
                        <li>
                            Disable - Disables the current priority from being seen / calculated in predictions
                        </li>
                        <li>
                            Rename - Renames the priority
                        </li>
                        <li>
                            Change - Changes the backing action the priority is to use
                        </li>
                        <li>
                            Delete - Deletes the priority
                        </li>
                    </ul>
                </li>
                <li>
                    <strong>2.</strong> Properties - Allows you to set some <a href="">Properties</a> on the priority. This does not affect the priorities' calculations
                </li>
