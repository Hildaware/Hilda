        <div class="w-2/3">
            <h2 class="pb-4">
                Set Overview
            </h2>
            <ul class="basic-list">
                <li>
                    <strong>1.</strong> Set Actions (in order)
                    <ul class="ml-6">
                        <li>
                            Set Configuration - Opens the Set <a href="">Configuration Settings Window</a>
                        </li>
                        <li>
                            Edit - Allows for setting a name, <a href="#version">version</a>, description, and author for the set
                        </li>
                        <li>
                            Record - Starts recording actions on use. This allows you to use actions and they'll be auto-populated to the set
                        </li>
                        <li>
                            Export - Copies the set in a base64 encoded string to your clipboard
                        </li>
                        <li>
                            Backups - Opens the <a href="">Backups Window</a>. Hilda backs up the latest 10 saves of a set
                        </li>
                        <li>
                            Delete - Deletes the set
                        </li>
                    </ul>
                </li>
                <li>
                    <strong>2.</strong> Add New Action - Adds a new action to the top of the priority set list
                </li>
                <li>
                    <strong>3.</strong> Debug - Helps with debugging an issue with the priority. This also opens the <a href="">Debug Window</a>
                </li>
                <li>
                    <strong>4.</strong> Priority Actions (in order)
                    <ul class="ml-6">
                        <li>
                            Edit - Allows for editing of the priorities' properties
                            <strong>** Non-Default Priority Sets Only</strong>
                        </li>
                        <li>
                            Duplicate - Creates a copy of the priority in the list
                        </li>
                        <li>
                            Export - Copies the priority in a base64 encoded string to your clipboard. This allows you to copy priorities across sets
                        </li>
                        <li>
                            Disable - Disables the current priority from being seen / calculated in predictions
                        </li>
                    </ul>
                </li>
                <li>
                    <strong>5.</strong> Properties - Controls when a Priority should be used based on <a href="">Conditions</a>
                </li>
                <li>
                    <strong>6.</strong> Move Up / Down - Moves a priority up/down in the list. The higher in the list, the higher the priority is. You can also drag the icon up/down
                </li>
                <li>
                    <strong>7.</strong> Save - Any modification needs to be saved. Text will display if a set requires saving.
                </li>
            </ul>
        </div>
